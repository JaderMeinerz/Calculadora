package org.example;

public class Calculadora {
    public static int soma(int[] numeros) {
        int a = numeros[0];
        int b = numeros[1];
        return a + b;
    }

    public static int subtracao(int [] numeros){
        int a = numeros[0];
        int b = numeros[1];
        return a - b;
    }

}
